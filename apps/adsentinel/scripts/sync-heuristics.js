// Copies packages/heuristics/heuristics.js into apps/adsentinel/lib/,
// stamped with a warning header. Cross-platform on purpose — this needs to
// work in Windows cmd.exe just as well as bash, so it's plain Node fs
// rather than a shell `cp`.
//
// Why this exists at all: Chrome only loads files that live inside the
// extension's own folder, so a shared package still needs a physical copy
// sitting in lib/ for AdSentinel to actually load it. See
// packages/heuristics/README.md for the full reasoning.
//
// Run from apps/adsentinel: `npm run sync`

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const source = join(__dirname, "..", "..", "..", "packages", "heuristics", "heuristics.js");
const dest = join(__dirname, "..", "lib", "heuristics.js");

const header = `// AUTO-GENERATED — DO NOT EDIT THIS FILE DIRECTLY.
// Source of truth: packages/heuristics/heuristics.js
// Regenerate with: npm run sync (from apps/adsentinel)
// Browser extensions can only load files inside their own folder, so this
// copy exists purely to satisfy that constraint — see
// packages/heuristics/README.md for why.

`;

const content = readFileSync(source, "utf8");
writeFileSync(dest, header + content);
console.log("Synced heuristics.js: packages/heuristics -> apps/adsentinel/lib");
